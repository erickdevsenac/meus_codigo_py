# Desafio 3
#  O usuário digita o nome, após digita 2 valores, após some e ao final
#  o programa imprime a seguinte frase: “Seu nome é: nome digitado pelo usuário 
#  e o valor da sua soma é: valor da soma dos números digitados pelo usuário”

nome_digitado = input('Digite seu nome: ')
valor1 = input('Digite um valor: ')
valor2 = input('Digite um valor: ')

resultado = int(valor1) + int(valor2)

print(f'Seu nome é: {nome_digitado} e o valor da sua soma é: {resultado}')



